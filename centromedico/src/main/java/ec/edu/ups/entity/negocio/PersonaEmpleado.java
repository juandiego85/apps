package ec.edu.ups.entity.negocio;

public class PersonaEmpleado {
	private int id;
	private String nombres;
	
	/**
	 * Encapsulamiento
	 * @return
	 */
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombres() {
		return nombres;
	}
	public void setNombres(String nombres) {
		this.nombres = nombres;
	}
	
	

}
